  static const empty = Currency(
    id: '',
    symbol: '',
    name: '',
    image: '',
    price: 0,
    marketCap: 0,
    marketCapRank: 0,
    priceChangePercentage24h: 0,
    marketCapChangePercentage24h: 0,
  );

